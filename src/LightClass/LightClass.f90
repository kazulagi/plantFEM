   function gaussian_function(x, mu, sigma1, sigma2) result(ret)
      real(real64), intent(in) :: x, mu, sigma1, sigma2
      real(real64) :: ret

      if (x < mu) then
         ret = exp(-0.50d0*((x - mu)**2)/(sigma1*sigma1))
      else
         ret = exp(-0.50d0*((x - mu)**2)/(sigma2*sigma2))
      end if
   end function

